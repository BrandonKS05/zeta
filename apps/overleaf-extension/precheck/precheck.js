    certificationStateFor,
    readinessStatusFor,
    generateReviewLedger,
    counterexampleExplanationForIssue,
    issueTitle,
    suggestedFixForIssue,
    reviewerCommentForIssue,
    problemSymbolsForReport,
    inDocumentReviewItems,
    buildReviewerSummaryContext,
    heuristicReviewerSummary,
    unavailableReviewerSummary,
