  const LATEX_ENV_RE = /\\begin\{(definition|theorem|lemma|assumption|proposition|corollary)\}(?:\[([^\]]+)\])?([\s\S]*?)\\end\{\1\}/g;
  const MATH_BLOCK_RE = /\$\$([\s\S]+?)\$\$|\$([^$]+?)\$|\\\[([\s\S]+?)\\\]|\\\(([\s\S]+?)\\\)/g;
  const COMMAND_RE = /\\([A-Za-z]+)/g;
