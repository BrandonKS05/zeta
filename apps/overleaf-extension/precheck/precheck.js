  function lineNumberAt(content, index) {
    return String(content || "").slice(0, Math.max(0, index)).split(/\r?\n/).length;
  }

  function compactWhitespace(value) {
    return String(value || "").replace(/\s+/g, " ").trim();
  }

  function contextWindow(content, start, end, radius = 100) {
    const left = Math.max(0, start - radius);
    const right = Math.min(content.length, end + radius);
    return compactWhitespace(content.slice(left, right));
  }

  function lineContextAt(content, index) {
    const text = String(content || "");
    const lineStart = text.lastIndexOf("\n", Math.max(0, index - 1)) + 1;
    const nextLine = text.indexOf("\n", index);
    const lineEnd = nextLine === -1 ? text.length : nextLine;
    return compactWhitespace(text.slice(lineStart, lineEnd));
  }

  function inferSemanticType(symbol, context) {
    const text = String(context || "").toLowerCase();
    const normalized = String(symbol || "").replace(/^\\/, "").toLowerCase();
    if (/covariance|matrix|\\mathbb\{r\}\^\{?[^}]*\\times/.test(text)) return "matrix";
    if (/scalar|variance|learning rate|step size|>\s*0|<\s*0|real number/.test(text)) return "scalar";
    if (/stopping|time parameter|parameter/.test(text)) return "parameter";
    if (/measure|probability|sigma.?algebra/.test(text)) return "measure";
    if (/function|operator|map|mapping|\\to|\\rightarrow/.test(text)) return "function";
    if (/set|collection|\\subset/.test(text)) return "set";
    if (/vector|random vector/.test(text)) return "vector";
    if (normalized === "sigma") return "symbol";
    if (normalized === "tau") return "parameter";
    return "unknown";
  }

  function isDefinitionContext(context) {
    return /\b(let|define|defined|denote|where|called)\b/i.test(context)
      && /\b(be|as|is|are|by|for)\b/i.test(context);
  }

  function extractEnvironments(file) {
    const content = file.content;
    const results = {
      definitions: [],
      theorems: [],
      lemmas: [],
      assumptions: [],
    };
    let match;
    while ((match = LATEX_ENV_RE.exec(content)) !== null) {
      const env = match[1];
      const title = compactWhitespace(match[2] || env);
      const body = compactWhitespace(match[3] || "");
      const item = {
        kind: env,
        title,
        text: body,
        file_path: file.file_path,
        line: lineNumberAt(content, match.index),
      };
      if (env === "definition") results.definitions.push(item);
      if (env === "theorem" || env === "proposition" || env === "corollary") results.theorems.push(item);
      if (env === "lemma") results.lemmas.push(item);
      if (env === "assumption") results.assumptions.push(item);
    }
    return results;
  }

  function extractInlineAssumptions(file) {
    const assumptions = [];
    const sentenceRe = /(?:^|[.!?]\s+)((?:Assume|Suppose)\b[^.!?]+[.!?]?)/g;
    let match;
    while ((match = sentenceRe.exec(file.content)) !== null) {
      assumptions.push({
        kind: "assumption",
        title: "Inline assumption",
        text: compactWhitespace(match[1]),
        file_path: file.file_path,
        line: lineNumberAt(file.content, match.index),
      });
    }
    return assumptions;
  }

  function extractSymbols(file) {
    const symbols = [];
    let mathMatch;
    while ((mathMatch = MATH_BLOCK_RE.exec(file.content)) !== null) {
      const mathText = mathMatch.slice(1).find(Boolean) || "";
      const blockStart = mathMatch.index;
      const blockEnd = blockStart + mathMatch[0].length;
      const context = contextWindow(file.content, blockStart, blockEnd);
      const localContext = lineContextAt(file.content, blockStart);
      let commandMatch;
      while ((commandMatch = COMMAND_RE.exec(mathText)) !== null) {
        const command = commandMatch[1];
        if (BUILTIN_COMMANDS.has(command)) continue;
        const symbol = `\\${command}`;
        const definition = isDefinitionContext(localContext);
        symbols.push({
          symbol,
          file_path: file.file_path,
          line: lineNumberAt(file.content, blockStart),
          context_text: context,
          semantic_type: inferSemanticType(symbol, context),
          is_definition: definition,
        });
      }
    }
    return symbols;
  }

  function parseLatexProject(filesInput) {
    const files = normalizeFiles(filesInput);
    const parsed = {
      documents: files.map((file) => ({ file_path: file.file_path, content_length: file.content.length })),
      definitions: [],
      theorems: [],
      lemmas: [],
      assumptions: [],
      symbols: [],
    };

    for (const file of files) {
      const envs = extractEnvironments(file);
      parsed.definitions.push(...envs.definitions);
      parsed.theorems.push(...envs.theorems);
      parsed.lemmas.push(...envs.lemmas);
      parsed.assumptions.push(...envs.assumptions, ...extractInlineAssumptions(file));
      parsed.symbols.push(...extractSymbols(file));
    }

    return parsed;
  }

  function detectNotationDrift(symbols) {
    const defsBySymbol = new Map();
    for (const item of symbols || []) {
      if (!item.is_definition) continue;
      if (!defsBySymbol.has(item.symbol)) defsBySymbol.set(item.symbol, []);
      defsBySymbol.get(item.symbol).push(item);
    }
    const issues = [];
    for (const [symbol, defs] of defsBySymbol.entries()) {
      const knownTypes = Array.from(new Set(defs.map((item) => item.semantic_type).filter((type) => type && type !== "unknown" && type !== "symbol")));
      const files = Array.from(new Set(defs.map((item) => item.file_path)));
      if (knownTypes.length > 1 && files.length > 1) {
        issues.push({
          id: `notation-drift-${symbol.replace(/[^A-Za-z0-9]/g, "")}`,
          type: "notation_drift",
          severity: "warning",
          symbol,
          message: `Potential issue: ${symbol} appears with multiple meanings (${knownTypes.join(", ")}) across ${files.join(", ")}.`,
          definitions: defs.map((item) => ({
            file_path: item.file_path,
            line: item.line,
            semantic_type: item.semantic_type,
            context_text: item.context_text,
          })),
        });
      }
    }
    return issues;
  }

  function detectUndefinedSymbols(symbols) {
    const bySymbol = new Map();
    for (const item of symbols || []) {
      if (!bySymbol.has(item.symbol)) bySymbol.set(item.symbol, []);
      bySymbol.get(item.symbol).push(item);
    }
    const issues = [];
    for (const [symbol, uses] of bySymbol.entries()) {
      const definitions = uses.filter((item) => item.is_definition);
      const sortedUses = uses.slice().sort((a, b) => a.file_path.localeCompare(b.file_path) || a.line - b.line);
      if (definitions.length === 0) {
        const first = sortedUses[0];
        issues.push({
          id: `undefined-${symbol.replace(/[^A-Za-z0-9]/g, "")}`,
          type: "undefined_symbol",
          severity: "warning",
          symbol,
          message: `Heuristic warning: ${symbol} is used but no local definition was detected.`,
          first_use: {
            file_path: first.file_path,
            line: first.line,
            context_text: first.context_text,
          },
        });
        continue;
      }
      const firstDefinition = definitions.slice().sort((a, b) => a.file_path.localeCompare(b.file_path) || a.line - b.line)[0];
      const earlierUse = sortedUses.find((item) => !item.is_definition && (
        item.file_path.localeCompare(firstDefinition.file_path) < 0
        || (item.file_path === firstDefinition.file_path && item.line < firstDefinition.line)
      ));
      if (earlierUse) {
        issues.push({
          id: `used-before-definition-${symbol.replace(/[^A-Za-z0-9]/g, "")}`,
          type: "undefined_symbol",
          severity: "warning",
          symbol,
          message: `Recommended author review: ${symbol} is used before its detected definition.`,
          first_use: {
            file_path: earlierUse.file_path,
            line: earlierUse.line,
            context_text: earlierUse.context_text,
          },
          first_definition: {
            file_path: firstDefinition.file_path,
            line: firstDefinition.line,
            context_text: firstDefinition.context_text,
          },
        });
      }
    }
    return issues;
  }

  function analyzePrecheck(parsed, options = {}) {
    const notationWarnings = detectNotationDrift(parsed.symbols);
    const undefinedSymbols = detectUndefinedSymbols(parsed.symbols);
    const verificationIssues = Array.isArray(options.verificationIssues) ? options.verificationIssues : [];
    return {
      notationWarnings,
      undefinedSymbols,
      verificationIssues,
      allIssues: [...notationWarnings, ...undefinedSymbols, ...verificationIssues],
    };
  }

  function calculateReadinessScore(parsed, analysis) {
    let score = 100;
    score -= analysis.notationWarnings.length * 12;
    score -= analysis.undefinedSymbols.length * 10;
    score -= analysis.verificationIssues.length * 14;
    if (parsed.definitions.length === 0) score -= 5;
    if (parsed.theorems.length + parsed.lemmas.length === 0) score -= 5;
    if (parsed.assumptions.length === 0) score -= 4;
    return Math.max(0, Math.min(100, score));
  }

  function certificationStateFor(score, analysis, options = {}) {
    const highSeverity = analysis.allIssues.some((issue) => issue.severity === "error");
    if (options.checking) return CERTIFICATION_STATES.CHECKING;
    if (options.notChecked) return CERTIFICATION_STATES.NOT_CHECKED;
    if (options.demoMode && !highSeverity && score >= 75) return CERTIFICATION_STATES.CERTIFIED_DEMO_MODE;
    if (highSeverity || score < 75) return CERTIFICATION_STATES.NEEDS_REVIEW;
    return CERTIFICATION_STATES.PRECHECK_PASSED;
  }

